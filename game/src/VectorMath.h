inline bool     operator!=(Vector3  a, Vector3 b) { return      a.x != b.x || a.y != b.y || a.z != b.z;       }

Vector2 Vector2SetLength(Vector2 v, float newLength);
Vector2 Vector2SetDistance(Vector2 v, float newLength, Vector2 from);

inline bool CheckCollisionPointRecV(Vector2 point, Vector2 position, Vector2 size)
{
	bool result =
		point.x >= position.x &&
		point.x <= position.x + size.x &&
		point.y >= position.y &&
		point.y <= position.y + size.y;

	return result;
}

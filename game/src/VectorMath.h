inline Vector2& operator/=(Vector2& a, float div) { return (a = Vector2Scale(a, 1.0f / div));   }

inline bool     operator==(Vector2  a, Vector2 b) { return      a.x == b.x && a.y == b.y;       }
inline bool     operator!=(Vector2  a, Vector2 b) { return      a.x != b.x || a.y != b.y;       }

